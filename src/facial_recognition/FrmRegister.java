    private void TextChangeEvent(){       
        textChangeListener = new DocumentListener(){
            @Override
            public void insertUpdate(DocumentEvent e) {
                updateTextFields();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateTextFields();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                updateTextFields();
            }
            private void updateTextFields(){
                if(txtHo.getText().isEmpty() || txtTenLotTen.getText().isEmpty() || txtSoDienThoai.getText().isEmpty() || 
                   txtSoDienThoai.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtEmail.getText().isEmpty() || ChonGioiTinh() == null)
                    CreateAccountFieldStatus(false);
                else 
                    CreateAccountFieldStatus(true);                  
            }
        };
        txtHo.getDocument().addDocumentListener(textChangeListener);
        txtTenLotTen.getDocument().addDocumentListener(textChangeListener);
        txtSoDienThoai.getDocument().addDocumentListener(textChangeListener);
        txtDiaChi.getDocument().addDocumentListener(textChangeListener);
        txtEmail.getDocument().addDocumentListener(textChangeListener);
        txtTaiKhoan.getDocument().addDocumentListener(textChangeListener);
        passfMatKhau.getDocument().addDocumentListener(textChangeListener);
        passfNhapLaiMatKhau.getDocument().addDocumentListener(textChangeListener);
    }
    
