    private static KeyListener OnlyCharTextField(){
        KeyListener keyListener = new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                 if (!Character.isLetter(c) && c != ' ') {
                    e.consume(); 
                }
            }
            @Override
            public void keyPressed(KeyEvent e) {
                // Not used in this example
            }
            @Override
            public void keyReleased(KeyEvent e) {
                // Not used in this example
            }
        };
        return keyListener;
    }    
    
    private static KeyListener OnlyNumberTextField(){
        KeyListener keyListener = new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c)) {
                    e.consume();  // Ignore non-character keys
                }
            }
            @Override
            public void keyPressed(KeyEvent e) {
                // Not used in this example
            }
            @Override
            public void keyReleased(KeyEvent e) {
                // Not used in this example
            }
        };
        return keyListener;
    }
    
    private boolean CheckSDT(String sdt){
        String regex = "^(\\+?84|0)(3[2-9]|5[2689]|7[06789]|8[1-689]|9[0-46-8])[0-9]{7}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(sdt);
        if (!matcher.matches()){
            JOptionPane.showMessageDialog(null, "Số điện thoại không đúng định dạng");
            return false;
        }
        return true;
    }
    
    private boolean CheckEmail(String email){
        String regex = "^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(email);
        if (!matcher.matches()) {
            JOptionPane.showMessageDialog(null, "Email không đúng định dạng");
            return false;
        }
        return true;
    }
    
